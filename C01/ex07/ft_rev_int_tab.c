void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int keep_int;

	i = 0;
	while (i != size / 2)
	{
		keep_int = *(tab + i);
		*(tab + i) = *(tab + size - 1 - i);
		*(tab + size - 1 - i) = keep_int;
		i++;
	}
}
