int		ft_find_next_prime(int nb)
{
	unsigned int i;

	i = 3;
	if (nb < 3)
		return (2);
	if (!(nb % 2))
		nb++;
	while (i * i <= (unsigned int)nb)
	{
		if (!(nb % i))
		{
			nb += 2;
			i = 1;
		}
		i += 2;
	}
	return (nb);
}
